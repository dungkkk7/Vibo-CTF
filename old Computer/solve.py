# Chuỗi secret được định nghĩa trong chương trình
secret = [
    0xd9, 0xfe, 0xc5, 0xf4, 0x9e, 199, 0x9b, 0xdc, 0xe7, 0xd2,
    0xf4, 0x93, 0xfe, 0xff, 0xf4, 0x9e, 0xde, 0xf9, 0xce, 0xe7,
    0xd2
]

# Key được sử dụng để XOR
key = 0xab

# Tạo mảng mới là secret XOR với key
secret = [i ^ key for i in secret]

# Chuyển đổi từng byte thành ký tự và in ra
result = '   '.join([hex(i) for i in secret])

# Kết quả sau khi chuyển đổi từng byte thành ký tự -> Flag{rUn_5l0wLy_8UT_5uReLy}
# nếu in ở đây 0x5f sẽ là chữ è -> tươgn ứng với "_" trong bảng mã ASCII
# 0x72   0x55   0x6e   0x5f   0x35   0x6c   0x30   0x77   0x4c   0x79   0x5f   0x38   0x55   0x54   0x5f   0x35   0x75   0x52   0x65   0x4c   0x79
# In ra kết quả
print(result)
